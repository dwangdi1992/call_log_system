package rma.org.bt.mms.model;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

public class DepositInfoDTO implements Serializable {

    //region private variables.
    private String cid;
    private String accountNo;
    private String depositType;
    private Date maturityDate;
    private BigDecimal amount;
    private BigDecimal maturityAmt;
    private String finInstitution;
    //endregion

    //region getter and setter.

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getDepositType() {
        return depositType;
    }

    public void setDepositType(String depositType) {
        this.depositType = depositType;
    }

    public Date getMaturityDate() {
        return maturityDate;
    }

    public void setMaturityDate(Date maturityDate) {
        this.maturityDate = maturityDate;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public BigDecimal getMaturityAmt() {
        return maturityAmt;
    }

    public void setMaturityAmt(BigDecimal maturityAmt) {
        this.maturityAmt = maturityAmt;
    }

    public String getFinInstitution() {
        return finInstitution;
    }

    public void setFinInstitution(String finInstitution) {
        this.finInstitution = finInstitution;
    }
    //endregion
}
